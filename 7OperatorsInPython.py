# Let's talk about Operators.
# OPERATORS :
# Python has different types of operators for different operations.To create a calculator we require arithmetic operators.
# ARITHMETIC OPERATORS :
# Operator       Operator Name        Example
# +                Addition              15+7
# -                Subtraction           15-7
# *                Multiplication        15*7
# /                Division              14/7
# %                Modulus               15%7
# //               Floor Division        15//7
# Some Applications :-
print(15+7)
print(15-7)
print(15*7)
print(14/7)
print(15%7)
print(14//7)
print(22//7)

