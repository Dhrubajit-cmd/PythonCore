# In this class we shall learn about short hand if-else statements in Python.
# Use only if the condition is simple. Not meant for it's complex use as it destroys the readability of the code.
a = 100
b = 200
print("A") if a > b else print("=") if a == b else print("B")

print(a) if a > b else print('=') if a == b else print(b)

print(9) if a > b else print(0)


entry = input("Enter your name: \n")
print("Morning. \n") if entry == "Dhrubajit" else print("Who are you ? \n")


