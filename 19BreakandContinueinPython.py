# In this class we shall learn about break and continue in python.

## Break Statement:
# The break statement enables a program to skip over a part of the code. A break statement terminates the very loop it lies within.
# Example :
for i in range(13):
    print(f"The value is {i} \n")
    if i == 10 :
        break
    print(i)
# See the output and you will understand.  Simple see !!
for i in range(13):
    print(f"The value is {i} \n")
    if i == 10 :
        continue
    print(i)
# See the output you will understand.

